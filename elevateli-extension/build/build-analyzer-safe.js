/**
 * Build script to bundle analyzer modules into a single file (SAFE VERSION)
 * This version uses innerHTML-free modules for Chrome Web Store compliance
 */

const fs = require('fs');
const path = require('path');
const { validateModules } = require('./validate-syntax');

// Define the order of modules to bundle
const moduleOrder = [
  'constants.js',
  'state.js',
  'chromeUtils.js',
  'memoryManager.js',
  'domUtils.js',
  'profileDiscovery.js',
  // Core modules
  'core/debug-config.js',
  'core/smart-logger.js',
  'core/logger.js',
  'core/cache-manager.js',
  // Extractors
  'extractors/base-extractor.js',
  'extractors/photo.js',
  'extractors/headline.js',
  'extractors/about.js',
  'extractors/experience.js',
  'extractors/skills.js',
  'extractors/education.js',
  'extractors/recommendations.js',
  'extractors/certifications.js',
  'extractors/projects.js',
  'extractors/featured.js',
  // Scoring modules
  'scoring/completeness-scorer.js',
  'scoring/quality-scorer.js',
  // UI modules - USING SAFE VERSION
  'ui/overlay-manager-safe.js',  // Changed from overlay-manager.js
  // Main analyzer class
  'analyzer-base.js',
  // Main entry point
  'analyzer-main.js'
];

// Read and process each module
let bundledContent = `/**
 * ElevateLI Content Script - Auto-generated Bundle (SAFE VERSION)
 * 
 * Generated on: ${new Date().toISOString()}
 * Version: ${require('../manifest.json').version}
 * Total modules: ${moduleOrder.length}
 * 
 * CHROME WEB STORE COMPLIANT VERSION - No innerHTML usage
 * 
 * DO NOT EDIT THIS FILE DIRECTLY - Edit the source modules instead
 * 
 * Build command: node build/build-analyzer-safe.js
 * Source directory: src/content/modules/
 * 
 * Included modules:
${moduleOrder.map((m, i) => ` * ${(i + 1).toString().padStart(2, '0')}. ${m}`).join('\n')}
 */

(function() {
  'use strict';

`;

// Validate syntax before building
const basePath = path.join(__dirname, '..', 'src', 'content', 'modules');
const validation = validateModules(moduleOrder, basePath);

if (validation.hasErrors) {
  console.error('\n❌ Build aborted due to syntax errors');
  console.error(`Found ${validation.errorCount} syntax error(s)`);
  console.error('\nRun "node build/validate-syntax.js" for detailed error information');
  process.exit(1);
}

console.log('\n✅ Syntax validation passed');
console.log('🛡️  Building SAFE version for Chrome Web Store compliance\n');

// Process each module
moduleOrder.forEach((modulePath, index) => {
  const fullPath = path.join(__dirname, '..', 'src', 'content', 'modules', modulePath);
  
  // Check if file exists
  if (!fs.existsSync(fullPath)) {
    console.error(`❌ File not found: ${fullPath}`);
    process.exit(1);
  }
  
  let moduleContent = fs.readFileSync(fullPath, 'utf8');
  
  // Remove import statements
  moduleContent = moduleContent.replace(/^import\s+.*?from\s+['"].*?['"];?\s*$/gm, '');
  
  // Remove export statements but keep the content
  moduleContent = moduleContent.replace(/^export\s+{[^}]+};?\s*$/gm, '');
  moduleContent = moduleContent.replace(/^export\s+(const|let|var|function|class)/gm, '$1');
  moduleContent = moduleContent.replace(/^export\s+default\s+/gm, '');
  
  // Add module comment
  bundledContent += `
  /* ============================================
   * MODULE: ${modulePath}
   * Index: ${index + 1}/${moduleOrder.length}
   * Size: ${(moduleContent.length / 1024).toFixed(2)} KB
   * ============================================ */
`;
  
  // Add the processed module content
  bundledContent += moduleContent;
  bundledContent += '\n\n';
});

// Close the IIFE
bundledContent += '})();\n';

// Add footer comment
bundledContent = `${bundledContent.trim()}

/* ============================================
 * END OF BUNDLE (SAFE VERSION)
 * Total size: ${(bundledContent.length / 1024).toFixed(2)} KB
 * Build completed: ${new Date().toISOString()}
 * Chrome Web Store compliant - No innerHTML usage
 * ============================================ */
`;

// Write the bundled file
const outputPath = path.join(__dirname, '..', 'src', 'content', 'analyzer.js');
fs.writeFileSync(outputPath, bundledContent);

console.log('✅ Successfully bundled analyzer modules (SAFE VERSION)');
console.log(`📦 Output: ${outputPath}`);
console.log(`📏 Size: ${(bundledContent.length / 1024).toFixed(2)} KB`);
console.log(`📄 Modules: ${moduleOrder.length} files bundled`);
console.log('🛡️  Chrome Web Store compliant - No innerHTML usage');

// Export moduleOrder for use by validate-syntax.js
module.exports = { moduleOrder };