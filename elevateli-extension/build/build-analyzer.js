/**
 * Build script to bundle analyzer modules into a single file
 * This works around Chrome's content script module limitations
 */

const fs = require('fs');
const path = require('path');

// Define the order of modules to bundle
const moduleOrder = [
  'constants.js',
  'state.js',
  '../../../common/ownership-detector.js',  // Shared ownership detection logic (from common)
  'chromeUtils.js',
  'memoryManager.js',
  'domUtils.js',
  'profileDiscovery.js',
  // Core modules
  'core/debug-config.js',
  'core/smart-logger.js',
  'core/logger.js',
  'core/cache-manager.js',
  'core/smart-cache-manager.js',
  // Extractors
  'extractors/base-extractor.js',
  'extractors/photo.js',
  'extractors/headline.js',
  'extractors/about.js',
  'extractors/experience.js',
  'extractors/skills.js',
  'extractors/education.js',
  'extractors/recommendations.js',
  'extractors/certifications.js',
  'extractors/projects.js',
  'extractors/featured.js',
  // Scoring modules
  'scoring/completeness-scorer.js',
  'scoring/quality-scorer.js',
  // Analysis modules
  'analysis/first-impression-analyzer.js',
  // UI modules
  'ui/overlay-manager.js',
  // Safe version - only for Chrome Web Store submission
  // 'ui/overlay-manager-safe.js',
  // Main analyzer class
  'analyzer-base.js',
  // Main entry point
  'analyzer-main.js'
];

// Read and process each module
let bundledContent = `/**
 * ElevateLI Content Script - Auto-generated Bundle
 * 
 * Generated on: ${new Date().toISOString()}
 * Version: ${require('../manifest.json').version}
 * Total modules: ${moduleOrder.length}
 * 
 * DO NOT EDIT THIS FILE DIRECTLY - Edit the source modules instead
 * 
 * Build command: node build/build-analyzer.js
 * Source directory: src/content/modules/
 * 
 * Included modules:
${moduleOrder.map((m, i) => ` * ${(i + 1).toString().padStart(2, '0')}. ${m}`).join('\n')}
 */

(function() {
  'use strict';

`;

// Helper function to resolve module paths
function resolveModulePath(modulePath) {
  if (modulePath.includes('../../../common/')) {
    // For common directory files
    return path.join(__dirname, '..', 'src', 'common', path.basename(modulePath));
  } else {
    // For regular module paths and other relative paths
    return path.join(__dirname, '..', 'src', 'content', 'modules', modulePath);
  }
}

// Create array of actual file paths for validation
const actualPaths = moduleOrder.map(module => resolveModulePath(module));

// Custom validation that uses actual paths
console.log('\\n🔍 Validating JavaScript syntax...\\n');
let hasErrors = false;
let errorCount = 0;

actualPaths.forEach((filePath, index) => {
  try {
    if (!fs.existsSync(filePath)) {
      console.log(`❌ File not found: ${moduleOrder[index]}`);
      hasErrors = true;
      errorCount++;
      return;
    }
    
    let content = fs.readFileSync(filePath, 'utf8');
    // Remove import/export statements
    content = content.replace(/^import\s+.*?from\s+['"].*?['"];?\s*$/gm, '');
    content = content.replace(/^export\s+{[^}]+};?\s*$/gm, '');
    content = content.replace(/^export\s+(const|let|var|function|class)/gm, '$1');
    content = content.replace(/^export\s+default\s+/gm, '');
    
    // Try to parse
    new (require('vm').Script)(content, { filename: filePath });
    console.log(`✅ ${moduleOrder[index]}`);
  } catch (error) {
    console.log(`❌ ${moduleOrder[index]}: ${error.message}`);
    hasErrors = true;
    errorCount++;
  }
});

const validation = { hasErrors, errorCount };

if (validation.hasErrors) {
  console.error('\n❌ Build aborted due to syntax errors');
  console.error(`Found ${validation.errorCount} syntax error(s)`);
  console.error('\nRun "node build/validate-syntax.js" for detailed error information');
  process.exit(1);
}

console.log('\n✅ Syntax validation passed\n');

// Process each module
moduleOrder.forEach((modulePath, index) => {
  // Use the same path resolution function
  const fullPath = resolveModulePath(modulePath);
  let moduleContent = fs.readFileSync(fullPath, 'utf8');
  
  // Remove import statements
  moduleContent = moduleContent.replace(/^import\s+.*?from\s+['"].*?['"];?\s*$/gm, '');
  
  // Remove export statements but keep the content
  moduleContent = moduleContent.replace(/^export\s+{[^}]+};?\s*$/gm, '');
  moduleContent = moduleContent.replace(/^export\s+(const|let|var|function|class)/gm, '$1');
  moduleContent = moduleContent.replace(/^export\s+default\s+/gm, '');
  
  // Add module comment
  bundledContent += `
  /* ============================================
   * MODULE: ${modulePath}
   * Index: ${index + 1}/${moduleOrder.length}
   * Size: ${(moduleContent.length / 1024).toFixed(2)} KB
   * ============================================ */
`;
  
  // Add the processed module content
  bundledContent += moduleContent;
  bundledContent += '\n\n';
});

// Close the IIFE
bundledContent += '})();\n';

// Add footer comment
bundledContent = `${bundledContent.trim()}

/* ============================================
 * END OF BUNDLE
 * Total size: ${(bundledContent.length / 1024).toFixed(2)} KB
 * Build completed: ${new Date().toISOString()}
 * ============================================ */
`;

// Write the bundled file
const outputPath = path.join(__dirname, '..', 'src', 'content', 'analyzer.js');
fs.writeFileSync(outputPath, bundledContent);

console.log('✅ Successfully bundled analyzer modules');
console.log(`📦 Output: ${outputPath}`);
console.log(`📏 Size: ${(bundledContent.length / 1024).toFixed(2)} KB`);
console.log(`📄 Modules: ${moduleOrder.length} files bundled`);

// Export moduleOrder for use by validate-syntax.js
module.exports = { moduleOrder };